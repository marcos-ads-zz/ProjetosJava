package modulo.loja;

/**
 *
 * @author Marcos Junior
 */
public class Loja {

    private int id;
    private String nome_loja;
    private int numero_filial;
    private int num_ponto;
    private String is_estadual;
    private String telefone;
    private String cnpj;
    private String cep;
    private String estado;
    private String cidade;
    private String bairro;
    private String logradouro;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome_loja() {
        return nome_loja;
    }

    public void setNome_loja(String nome_loja) {
        this.nome_loja = nome_loja;
    }

    public int getNumero_filial() {
        return numero_filial;
    }

    public void setNumero_filial(int numero_filial) {
        this.numero_filial = numero_filial;
    }

    public int getNum_ponto() {
        return num_ponto;
    }

    public void setNum_ponto(int num_ponto) {
        this.num_ponto = num_ponto;
    }

    public String getIs_estadual() {
        return is_estadual;
    }

    public void setIs_estadual(String is_estadual) {
        this.is_estadual = is_estadual;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getLogradouro() {
        return logradouro;
    }

    public void setLogradouro(String logradouro) {
        this.logradouro = logradouro;
    }

}
